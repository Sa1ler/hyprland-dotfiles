-- Настройка стандартных сочетаний клавиш для Neovim
local map = vim.api.nvim_set_keymap
local opts = { noremap = true, silent = true }

-- ==========================
-- Копирование (Copy)
-- ==========================
-- Визуальный режим: копирование выделенного текста в системный буфер
map('v', '<C-c>', '"+y', opts)
-- Нормальный режим: копирование текущей строки в системный буфер
map('n', '<C-c>', '"+yy', opts)

-- ==========================
-- Вставка (Paste)
-- ==========================
-- Нормальный режим: вставка из системного буфера
map('n', '<C-v>', '"+p', opts)
-- Вставочный режим: вставка из системного буфера
map('i', '<C-v>', '<C-r>+', opts)
-- Визуальный режим: вставка заменяет выделение (по желанию)
map('v', '<C-v>', '"+p', opts)

-- ==========================
-- Вырезание (Cut)
-- ==========================
-- Визуальный режим: вырезание в системный буфер
map('v', '<C-x>', '"+d', opts)
-- Нормальный режим: вырезание текущей строки в системный буфер
map('n', '<C-x>', '"+dd', opts)

-- ==========================
-- Выделение всего текста (Select All)
-- ==========================
-- Нормальный режим: выделяем весь текст
map('n', '<C-a>', 'ggVG', opts)
-- Визуальный режим: тоже можно использовать (опционально)
map('v', '<C-a>', 'ggVG', opts)
